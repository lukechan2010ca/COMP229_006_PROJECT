class AdModel
{
    constructor(id, title, description, tags, price, expirationDate, isActive){
        this.id = id;
        this.title = title;
        this.description = description;
        this.tags = tags;
        this.price = price;
        this.expirationDate = expirationDate;
        this.isActive = isActive;
    }
}

export default AdModel;

